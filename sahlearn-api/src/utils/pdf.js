// ATTENDANCE REGISTER
// ════════════════════════════════════════════════════════════════════════════
const generateAttendanceRegister = (res, { session, roster }) => {
  const doc = new PDFDocument({ size: 'A4', margin: 40, bufferPages: true });
  const dateStr = new Date(session.date).toLocaleDateString('en-NG', { day: 'numeric', month: 'long', year: 'numeric' });
  sendPDF(res, doc, `attendance-${dateStr.replace(/ /g, '-')}.pdf`);

  drawHeader(doc, 'Attendance Register', `${session.course?.title || ''} · ${dateStr}`);

  sectionHeading(doc, session.label);

  if (session.note) {
    doc.fillColor(GREY).fontSize(8).font('Helvetica-Oblique').text(`Note: ${session.note}`, 48, doc.y);
    doc.moveDown(0.6);
  }

  const cols = [
    { label: '#', width: 30 },
    { label: 'Student ID', width: 100 },
    { label: 'Full Name', width: 220 },
    { label: 'Status', width: 80 },
    { label: 'Signature', width: 90 },
  ];
  tableHeader(doc, cols);

  roster.forEach((s, i) => {
    tableRow(doc, cols, [i + 1, s.studentCode, s.fullName, STATUS_LABEL[s.status] || s.status, ''], i % 2 === 0);
  });

  // Summary
  doc.moveDown(0.8);
  const counts = { present: 0, late: 0, excused: 0, absent: 0 };
  for (const s of roster) counts[s.status] = (counts[s.status] || 0) + 1;
  doc.fillColor(DARK).fontSize(9).font('Helvetica-Bold')
    .text(`Total: ${roster.length}   Present: ${counts.present}   Late: ${counts.late}   Excused: ${counts.excused}   Absent: ${counts.absent}`, 48, doc.y);

  const range = doc.bufferedPageRange();
  for (let i = range.start; i < range.start + range.count; i++) {
    doc.switchToPage(i);
    drawFooter(doc);
  }

  doc.end();
};

